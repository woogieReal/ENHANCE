package com.sist.feb.category.domain;

import com.sist.feb.category.cmn.DTO;

/**
 * @author 임하람
 * 카테고리 구분 VO
 */
public class SearchVO extends DTO {
	private String selectDiv;//카테고리 구분
	
	public SearchVO() { }//default 생성자


	public SearchVO(String selectDiv) {
		super();
		this.selectDiv = selectDiv;
	}


	public String getSelectDiv() {
		return selectDiv;
	}


	public void setSelectDiv(String selectDiv) {
		this.selectDiv = selectDiv;
	}


	@Override
	public String toString() {
		return "SearchVO [selectDiv=" + selectDiv + ", toString()=" + super.toString() + "]";
	}
	
	
	

	
	

}//--class
