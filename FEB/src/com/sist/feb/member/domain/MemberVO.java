package com.sist.feb.member.domain;

import com.sist.feb.member.cmn.DTO;

public class MemberVO extends DTO{

	private int    member_no; //멤버번호
	private String name;      //이름
	private String email;     //이메일
	private String pw;        //비밀번호
	private String location;  //위치
	private String intro;     //소개글
	private String reg_dt;    //등록일
	
	public MemberVO() {
		
	}

	public MemberVO(int member_no, String name, String email, String pw, String location, String intro, String reg_dt) {
		super();
		this.member_no = member_no;
		this.name = name;
		this.email = email;
		this.pw = pw;
		this.location = location;
		this.intro = intro;
		this.reg_dt = reg_dt;
	}

	public int getMember_no() {
		return member_no;
	}

	public void setMember_no(int member_no) {
		this.member_no = member_no;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getPw() {
		return pw;
	}

	public void setPw(String pw) {
		this.pw = pw;
	}

	public String getLocation() {
		return location;
	}

	public void setLocation(String location) {
		this.location = location;
	}

	public String getIntro() {
		return intro;
	}

	public void setIntro(String intro) {
		this.intro = intro;
	}

	public String getReg_dt() {
		return reg_dt;
	}

	public void setReg_dt(String reg_dt) {
		this.reg_dt = reg_dt;
	}

	@Override
	public String toString() {
		return "MemberVO [member_no=" + member_no + ", name=" + name + ", email=" + email + ", pw=" + pw + ", location="
				+ location + ", intro=" + intro + ", reg_dt=" + reg_dt + ", toString()=" + super.toString() + "]";
	}
	
	
	
}
